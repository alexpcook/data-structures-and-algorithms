package stack

// Stacks operate on a last-in, first-out (LIFO) paradigm
// - Lookup O(n) (not a typical operation)
// - Pop O(1) (remove top item)
// - Push O(1) (add item to top)
// - Peek O(1) (view the topmost item in the stack)

// Stacks can be implemented using either arrays or linked lists
// Arrays work well because you can push/pop entries off the end
// Linked lists also work if you push/pop entries from the front of the linked list
